package chapter1;

public class Variable {
	public static void main(String[] args){
		char sex='女';
		boolean bool=true;
		byte b1=12;short s1=127;
		int i=35;
		long l=123456;
		float fl=3.14f;
		double dl=3.14;
		System.out.println("字符型变量sex的值为:"+sex);
		System.out.println("布尔类型变量bool的值为:"+bool);
		System.out.println("字节整型变量byte的值为:"+b1);
		System.out.println("短整型变量short的值为:"+s1);
		System.out.println("整型变量i的值为:"+i);
		System.out.println("长整型变量l的值为:"+l);
		System.out.println("单精度浮点型变量f1的值为:"+fl);
		System.out.println("双精度浮点型变量dl的值为:"+dl);
			}

}
